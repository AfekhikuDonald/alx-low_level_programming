#include <stdio.h>


/**
 * add - sums two numbers
 * @a: number
 * @b: number
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtracts two numbers
 * @a: number
 * @b: number
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiplies two numbers
 * @a: number
 * @b: number
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divides two numbers
 * @a: number
 * @b: number
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - finds the modulos of two numbers
 * @a: number
 * @b: number
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
